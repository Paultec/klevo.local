$(function(){
    /******************************************************************************
     Horizontal menu
     *******************************************************************************/

    var menuItem = $('.horizontal-nav a');

    menuItem.hover(
        function() {
        $(this).css({'color': '#563D7C'})
               .children().css({'color': '#428BCA'});
    },
        function() {
            $(this).css({'color': '#428BCA'})
                .children().css({'color': '#563D7C'});
        }
    );

    /******************************************************************************
     Info on carousel
     *******************************************************************************/

    $('.items').mosaic({
        animation: 'slide'
    });

    /******************************************************************************
     accordion
     *******************************************************************************/
    $('.accordion-group').has('ul').find('span').addClass('glyphicon-plus');

    /******************************************************************************
     modal
     *******************************************************************************/
    $('#modal').leanModal({ top : 200, closeButton: ".modal_close" });
});
